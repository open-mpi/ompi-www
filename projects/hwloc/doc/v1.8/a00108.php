<p>Get the hwloc OS device object corresponding to the CUDA device whose index is <code>idx</code>. </p>
<p>Return the OS device object describing the CUDA device whose index is <code>idx</code>. Return NULL if there is none.</p>
<p>The topology <code>topology</code> does not necessarily have to match the current machine. For instance the topology may be an XML import of a remote host. I/O devices detection and the CUDA component must be enabled in the topology. If not, the locality of the object may still be found using <a class="el" href="a00108.php#ga6d8369da45101fbf88b296373b431e49" title="Get the CPU set of logical processors that are physically close to device idx. ">hwloc_cudart_get_device_cpuset()</a>.</p>
<dl class="section note"><dt>Note</dt><dd>The corresponding PCI device object can be obtained by looking at the OS device parent object.</dd>
<dd>
This function is identical to <a class="el" href="a00107.php#ga108c1a96b356e5a0b236763d4d31fbeb" title="Get the hwloc OS device object corresponding to the CUDA device whose index is idx. ">hwloc_cuda_get_device_osdev_by_index()</a>. </dd></dl>

