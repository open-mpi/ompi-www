<p>Get the hwloc OS device object corresponding to OpenCL device <code>device</code>. </p>
<p>Return the hwloc OS device object that describes the given OpenCL device <code>device</code>. Return NULL if there is none.</p>
<p>Topology <code>topology</code> and device <code>device</code> must match the local machine. I/O devices detection and the OpenCL component must be enabled in the topology. If not, the locality of the object may still be found using <a class="el" href="a00086.php#ga94b79d9c339f463fa2891e941ab50a7f" title="Get the CPU set of logical processors that are physically close to OpenCL device device.">hwloc_opencl_get_device_cpuset()</a>.</p>
