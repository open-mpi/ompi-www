<p>Get the CPU set of logical processors that are physically close to OpenCL device <code>device</code>. </p>
<p>Return the CPU set describing the locality of the OpenCL device <code>device</code>.</p>
<p>Topology <code>topology</code> and device <code>device</code> must match the local machine. I/O devices detection and the OpenCL component are not needed in the topology.</p>
<p>The function only returns the locality of the device. If more information about the device is needed, OS objects should be used instead, see <a class="el" href="a00086.php#ga8405f20e119f3646a171395c337a712c" title="Get the hwloc OS device object corresponding to OpenCL device device.">hwloc_opencl_get_device_osdev()</a> and <a class="el" href="a00086.php#ga4a03b63357561e3238535c039d64a2f0" title="Get the hwloc OS device object corresponding to the OpenCL device for the given indexes.">hwloc_opencl_get_device_osdev_by_index()</a>.</p>
<p>This function is currently only implemented in a meaningful way for Linux with the AMD OpenCL implementation; other systems will simply get a full cpuset. </p>

