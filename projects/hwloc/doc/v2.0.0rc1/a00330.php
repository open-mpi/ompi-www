<div class="textblock"><p>hwloc topologies may be annotated with Misc objects (of type <code><a class="el" href="a00160.php#ggacd37bb612667dc437d66bfb175a8dc55a19f8a6953fa91efc76bcbcdf2d22de4d" title="Miscellaneous objects (filtered out by default). Objects without particular meaning, that can e.g. be added by the application for its own use, or by hwloc for miscellaneous objects such as MemoryModule (DIMMs). These objects are not listed in the main children list, but rather in the dedicated misc children list. Misc objects may only have Misc objects as children, and those are in the dedicated misc children list as well. Misc objects have NULL CPU and node sets. ">HWLOC_OBJ_MISC</a></code>) either automatically or by the user. This is an flexible way to annotate topologies with large sets of information since Misc objects may be inserted anywhere in the topology (to annotate specific objects or parts of the topology), even below other Misc objects, and each of them may contain multiple attributes (ee also <a class="el" href="a00338.php#faq_annotate">How do I annotate the topology with private notes?</a>).</p>
<p>These Misc objects may have a <code>subtype</code> field to replace <code>Misc</code> with something else in the lstopo output.</p>
<h1><a class="anchor" id="miscobjs_auto"></a>
Misc objects added by hwloc</h1>
<p>hwloc only uses Misc objects when other object types are not sufficient, and when the Misc object type is not filtered-out anymore. This currently includes: </p><ul>
<li>
Memory modules (DIMMs), on Linux when privileged and when <code>dmi-sysfs</code> is supported by the kernel. These objects have a <code>subtype</code> field of value <code>MemoryModule</code>. They are currently always attached to the root object. Their attributes describe the DIMM vendor, model, etc. <code>lstopo -v</code> displays them as: <div class="fragment"><div class="line">Misc(MemoryModule) (P#1 DeviceLocation=<span class="stringliteral">&quot;Bottom-Slot 2(right)&quot;</span> BankLocation=<span class="stringliteral">&quot;BANK 2&quot;</span> Vendor=Elpida SerialNumber=21733667 AssetTag=9876543210 PartNumber=<span class="stringliteral">&quot;EBJ81UG8EFU0-GN-F &quot;</span>)</div></div><!-- fragment -->  </li>
<li>
Displaying process binding in <code>lstopo --top</code>. These objects have a <code>subtype</code> field of value <code>Process</code> and a name attribute made of their PID and program name. They are attached below the object they are bound to. The textual <code>lstopo</code> displays them as: <div class="fragment"><div class="line">PU L#0 (P#0)</div><div class="line">  Misc(Process) 4445 myprogram</div></div><!-- fragment -->  </li>
</ul>
<h1><a class="anchor" id="miscobjs_annotate"></a>
Annotating topologies with Misc objects</h1>
<p>The user may annotate hwloc topologies with its own Misc objects. This can be achieved with <code><a class="el" href="a00170.php#gad980782ade737900c5cf208946768c30" title="Add a MISC object as a leaf of the topology. ">hwloc_topology_insert_misc_object()</a></code> as well as hwloc-annotate command-line tool. </p>
