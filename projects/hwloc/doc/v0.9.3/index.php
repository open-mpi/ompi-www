    <span class="keywordtype">unsigned</span> <span class="keywordtype">int</span> topodepth;
    <a class="code" href="group__hwlocality__topology.php#ga9d1e76ee15a7dee158b786c30b6a6e38" title="Topology context.">hwloc_topology_t</a> topology;
