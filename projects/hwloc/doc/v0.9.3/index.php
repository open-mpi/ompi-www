            free(str);
        }

        <span class="comment">/* Free our cpuset copy */</span>
        <a class="code" href="group__hwlocality__cpuset.php#gaf5d5a9e082a43f8311fdcff55e611b23" title="Free CPU set set.">hwloc_cpuset_free</a>(cpuset);
    }

    <span class="comment">/* Destroy topology object. */</span>
    <a class="code" href="group__hwlocality__creation.php#ga9f34a640b6fd28d23699d4d084667b15" title="Terminate and free a topology context.">hwloc_topology_destroy</a>(topology);

    <span class="keywordflow">return</span> 0;
}
