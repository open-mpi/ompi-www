        <span class="keywordflow">for</span> (i = 0; i &lt; <a class="code" href="group__hwlocality__information.php#gab17065e3d53455973844568d9f21c72c" title="Returns the width of level at depth depth.">hwloc_get_nbobjs_by_depth</a>(topology, depth); 
             i++) {
