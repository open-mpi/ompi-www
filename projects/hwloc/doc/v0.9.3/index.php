        <span class="comment">/* And try to bind ourself there. */</span>
        <span class="keywordflow">if</span> (<a class="code" href="group__hwlocality__binding.php#ga47053da286384d86ec3e4fb3fe148dae" title="Bind current process or thread on cpus given in cpuset set.">hwloc_set_cpubind</a>(topology, cpuset, 0)) {
