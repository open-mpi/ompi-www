<div class="memdoc">

<p>Compare the depth of two object types. </p>
<p>Types shouldn't be compared as they are, since newer ones may be added in the future. This function returns less than, equal to, or greater than zero if <code>type1</code> is considered to be respectively higher than, equal to, or deeper than <code>type2</code> in the hierarchy. If the types can not be compared (because it does not make sense), HWLOC_TYPE_UNORDERED is returned. Object types containing CPUs can always be compared.</p>
<dl class="note"><dt><b>Note:</b></dt><dd>HWLOC_OBJ_SYSTEM will always be the highest, and HWLOC_OBJ_PROC will always be the deepest. </dd></dl>

