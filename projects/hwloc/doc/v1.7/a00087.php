<p>Get the CPU set of logical processors that are physically close to device <code>cudevice</code>. </p>
<p>Return the CPU set describing the locality of the CUDA device <code>cudevice</code>.</p>
<p>Topology <code>topology</code> and device <code>cudevice</code> must match the local machine. I/O devices detection and the CUDA component are not needed in the topology.</p>
<p>The function only returns the locality of the device. If more information about the device is needed, OS objects should be used instead, see <a class="el" href="a00087.php#ga849dad87840346e77d135ecef1593416" title="Get the hwloc OS device object corresponding to CUDA device cudevice.">hwloc_cuda_get_device_osdev()</a> and <a class="el" href="a00087.php#ga108c1a96b356e5a0b236763d4d31fbeb" title="Get the hwloc OS device object corresponding to the CUDA device whose index is idx.">hwloc_cuda_get_device_osdev_by_index()</a>.</p>
<p>This function is currently only implemented in a meaningful way for Linux; other systems will simply get a full cpuset. </p>

