<p>Get the hwloc OS device object corresponding to CUDA device <code>cudevice</code>. </p>
<p>Return the hwloc OS device object that describes the given CUDA device <code>cudevice</code>. Return NULL if there is none.</p>
<p>Topology <code>topology</code> and device <code>cudevice</code> must match the local machine. I/O devices detection and the NVML component must be enabled in the topology. If not, the locality of the object may still be found using <a class="el" href="a00087.php#gae06cf330d2f0d9949feb52b146b7d136" title="Get the CPU set of logical processors that are physically close to device cudevice.">hwloc_cuda_get_device_cpuset()</a>.</p>
<dl class="section note"><dt>Note</dt><dd>The corresponding hwloc PCI device may be found by looking at the result parent pointer. </dd></dl>

